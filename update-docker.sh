# –ü–µ—Ä–µ–∑–∞–ø—É—Å—Ç–∏—Ç—å nginx, —á—Ç–æ–±—ã –æ–Ω –ø–æ–¥—Ö–≤–∞—Ç–∏–ª –∫–æ–Ω—Ñ–∏–≥–∏ –∏–∑ volume
