echo "üèÅ –°–∫—Ä–∏–ø—Ç –∑–∞–≤–µ—Ä—à–µ–Ω (—à–∞–≥–∏ Ansible/Nginx –ø—Ä–æ–ø—É—â–µ–Ω—ã)." 