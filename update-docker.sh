if [ $ANSIBLE_EXIT_CODE -ne 0 ]; then
  echo "‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–∏ Ansible! –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ª–æ–≥–∏."
  exit $ANSIBLE_EXIT_CODE
